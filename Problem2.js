// เขียนโปรแกรมรับอินพุต 1 ตัวที่เป็นจำนวนเต็ม และตรวจสอบว่าจำนวนที่รับมามีค่ามากกว่า 0 หรือไม่
// - ถ้ามีค่ามากกว่า 0 ให้พิมพ์ 'มากกว่า 0'
// - ถ้ามีค่าน้อยกว่าหรือเท่ากับ 0 ให้พิมพ์ 'น้อยกว่าหรือเท่ากับ 0'

function ZeroOrNot(number) {
  if (number > 0) {
    console.log("มากกว่า 0 จ้า");
  } else if (number <= 0) {
    console.log("น้อยกว่าหรือเท่ากับ 0 จ้า");
  }
}

ZeroOrNot(1);
ZeroOrNot(0);
ZeroOrNot(-1);
