// เขียนโปรแกรมรับอินพุต 1 ตัวเป็นอุณหภูมิในหน่วนฟาเรนไฮต์ (จำนวนจริง) และตรวจสอบว่า จำนวนที่รับมามีค่ามากกว่าหรือเท่ากับ 32 หรือไม่
// - ถ้ามีค่ามากกว่าหรือเท่ากับ 32 ให้คำนวณอุณหภูมิในหน่วนองศาเซลเซียส และพิมพ์ค่าออกมา
// ด้วยสูตร C=(5 x (F - 32)) / 9
// - ถ้ามีค่าน้อยกว่า 32 ให้พิมพ์ 'cold'

function temp(faTemp) {
  if (faTemp >= 32) {
    const Cel = (5 * (faTemp - 32)) / 9;
    console.log(Cel);
  } else {
    console.log("ฉันหนาวเหลือเกินพี่จ๋าา");
  }
}

temp(32);
temp(40);
temp(30);
