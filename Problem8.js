// เขียนโปรแกรมรับอินพุต 1 ตัวเป็นจำนวนเงินในหน่วยดอลล่าสหรัฐ (จำนวนจริง) และตรวจสอบว่า จำนวนที่รับมามีค่ามากกว่า 0 หรือไม่
// - ถ้ามีค่ามากกว่า 0 ให้คำนวณจำนวนเงินในหน่วนบาท (THB = USD x 32.5) และพิมพ์ค่าออกมา
// - ถ้ามีค่าน้อยกว่าหรือเท่ากับ 0 ให้พิมพ์ "you don't have money"

function usdToThai(dollar) {
  if (dollar > 0) {
    const THB = dollar * 32.5;
    console.log(THB);
  } else {
    console.log("ว้ายยยยย หมดตูด");
  }
}

usdToThai(100);
usdToThai(0);
usdToThai(-100);
